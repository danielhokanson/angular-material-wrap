// Get all component types that need to be imported
export function getAllComponentTypes(): Type<any>[] {
    const types: Type<any>[] = [];
    Object.values(DEMO_REGISTRY).forEach(components => {
        if (components.demo) types.push(components.demo);
        if (components.validation) types.push(components.validation);
        if (components.code) types.push(components.code);
        if (components.api) types.push(components.api);
    });
    return types;
}
